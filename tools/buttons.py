# Safe Dictionary Wrapper
class LanguageDict(dict):
    def __missing__(self, key):
        return DEFAULT_STRINGS.get(key, key)

# Helper to ensure "_" is always a dictionary
def ensure_lang(lang):
    if not lang:
        return LanguageDict()
    return lang

